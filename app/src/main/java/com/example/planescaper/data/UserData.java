package com.example.planescaper.data;

public class UserData {
    public String id;
    public String name;
    public String email;
    public String password;

    public UserData() {

    }

    public UserData(String id, String name, String email, String password) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.password = password;
    }
}
